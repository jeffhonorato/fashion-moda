const modalCard1BolsasFem = () => {
    const imgPequenas = document.querySelectorAll(".js-modal-imgP-bolsa-card1");
    imgPequenas.forEach((img) => {
        img.addEventListener("click", () => {
            const imgGrande = document.querySelector(".js-modal-imgG-bolsa-card1");
            imgGrande.src = img.src;
        });
    });

    const detalhesCard1 = document.querySelector(".js-btn1-detalhes-bolsa");
    detalhesCard1.addEventListener("click", (e) => {
        e.stopPropagation()
        const modalBox1 = document.querySelector(".js-modal__card1__bolsa");
        modalBox1.classList.add("show"); 
    });

    const btnFechar = document.querySelector(".js-btn-fechar-card1-bolsa");
    btnFechar.addEventListener("click", () => {
        const modalBox1 = document.querySelector(".js-modal__card1__bolsa");
        modalBox1.classList.remove("show");
    });

    
}

