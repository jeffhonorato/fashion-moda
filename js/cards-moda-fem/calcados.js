const modalCard1CalcadosFem = () => {
    const imgPequenas = document.querySelectorAll(".js-modal-imgP-calcados-card1");
    imgPequenas.forEach((img) => {
        img.addEventListener("click", () => {
            const imgGrande = document.querySelector(".js-modal-imgG-calcados-card1");
            imgGrande.src = img.src;
        });
    });

    const detalhesCard1 = document.querySelector(".js-btn1-detalhes-calcados");
    detalhesCard1.addEventListener("click", (e) => {
        e.stopPropagation()
        const modalBoxCard1 = document.querySelector(".js-modal__card1__calcados");
        modalBoxCard1.classList.add("show"); 
        
    });

    const btnFechar = document.querySelector(".js-btn-fechar-card1-calcados");
    btnFechar.addEventListener("click", (e) => {
        e.stopPropagation()
        const modalBoxCard1 = document.querySelector(".js-modal__card1__calcados");
        modalBoxCard1.classList.remove("show");
        
    });
    
}

