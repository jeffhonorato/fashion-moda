const modalCard1SaiasFem = () => {
    const imgPequenas = document.querySelectorAll(".js-modal-imgP-saia-card1");
    imgPequenas.forEach((img) => {
        img.addEventListener("click", () => {
            const imgGrande = document.querySelector(".js-modal-imgG-saia-card1");
            imgGrande.src = img.src;
        });
    });

    const detalhesCard1 = document.querySelector(".js-btn1-detalhes-saia");
    detalhesCard1.addEventListener("click", (e) => {
        e.stopPropagation()
        const modalBox1 = document.querySelector(".js-modal__card1__saia");
        modalBox1.classList.add("show"); 
        
        
    });

    const btnFechar = document.querySelector(".js-btn-fechar-card1-saia");
    btnFechar.addEventListener("click", () => {
        const modalBox1 = document.querySelector(".js-modal__card1__saia");
        modalBox1.classList.remove("show");
    });

    
}

