modalCard1ShortsMasc = () => {
    const imgPequenas = document.querySelectorAll(".js-modal-imgP-shortMasc-card1");
    imgPequenas.forEach((img) => {
        img.addEventListener("click", () => {
            const imgGrande = document.querySelector(".js-modal-imgG-shortMasc-card1");
            imgGrande.src = img.src;
        });
    });

    const detalhesCard1 = document.querySelector(".js-btn1-detalhes-shortMasc");
    detalhesCard1.addEventListener("click", (e) => {
        e.stopPropagation()
        const modalBox1 = document.querySelector(".js-modal__card1__shortMasc");
        modalBox1.classList.add("show");
    });

    const btnFechar = document.querySelector(".js-btn-fechar-card1-shortsMasc");
    btnFechar.addEventListener("click", (e) => {
        e.stopPropagation()
        const modalBox1 = document.querySelector(".js-modal__card1__shortMasc");
        modalBox1.classList.remove("show");
    });
}

